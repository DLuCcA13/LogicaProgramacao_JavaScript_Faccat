/*

Ler um valor e escrever a mensagem É MAIOR QUE 10!
se o valor lido for maior que 10, caso contrário escrever NÃO É MAIOR QUE 10!

*/

//Declarando variáveis e atrbuindo valores

//Variável "numero" para armazenar um numero
let numero = parseFloat(prompt("Digite um número: "));

//Verificando se o número é maior que 10
if (numero > 10) {

  //Se o número for maior que 10, exibir a mensagem
  console.log(numero + " É MAIOR QUE 10!");
}
//Caso contrário
else {
  //Se o número não for maior que 10, exibir a mensagem
  console.log(numero + " É MAIOR QUE 10!");
}