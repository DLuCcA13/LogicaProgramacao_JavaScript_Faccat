/*Escreva um algoritmo para ler as dimensões de um retângulo
(base e altura), calcular e escrever a área do retângulo.*/

// leitura do valor da base
let base = Number(prompt("Digite a base do retângulo: "));

// leitura do valor da altura
let altura = Number(prompt("Digite a altura do retângulo: "));

// calculo da area
let area = base * altura;

//saida dos dados
console.log(`a base do retângulo e ${base} e a altura e ${altura} e a area e ${area}`)